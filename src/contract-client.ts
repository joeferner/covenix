import type { ContractOperation, ContractResponse, SchemaNode, ZodecContract } from './contract.js';

/**
 * Generates a **standalone** TypeScript client from a {@link ZodecContract}. The
 * output is a single self-contained module — types reconstructed from the
 * contract's schema nodes plus a small inlined fetch runtime — so a frontend
 * imports one file with **no `zodec` dependency**.
 *
 * Operations are grouped by their first tag (`api.users.get(...)`); untagged
 * operations sit at the client root. Each method returns the success body and
 * throws a `ZodecClientError` on a non-2xx response; `method.raw(...)` returns a
 * status-discriminated `{ status, body, headers }` union (the `headers` is a
 * standard `Headers` object) for exhaustive handling and response-header access.
 *
 * Descriptions (`.describe()` / `.meta({ description })`) and operation
 * summaries become JSDoc on interfaces, fields, and methods. v1 emits types only
 * (no runtime response validation).
 *
 * @param contract - The contract to generate from (e.g. from `generateContract`).
 * @returns TypeScript source for the client module.
 */
export function generateTypeScriptClient(contract: ZodecContract): string {
  const { root, groups } = groupOperations(contract.operations);
  const header =
    `/* tslint:disable */\n` +
    `/* eslint-disable */\n` +
    `// Generated by zodec — do not edit.\n` +
    `// Regenerate from your controllers (contract version ${contract.zodecContract}).`;
  return (
    [
      header,
      emitSchemas(contract.schemas),
      RUNTIME,
      emitClientInterface(root, groups),
      emitFactory(root, groups),
    ].join('\n\n') + '\n'
  );
}

/** A named group of operations under a single client key (a tag). */
interface OperationGroup {
  key: string;
  operations: ContractOperation[];
}

/** Splits operations into root (untagged) and tag groups (keyed by first tag). */
function groupOperations(operations: ContractOperation[]): {
  root: ContractOperation[];
  groups: OperationGroup[];
} {
  const root: ContractOperation[] = [];
  const byKey = new Map<string, ContractOperation[]>();
  for (const op of operations) {
    const tag = op.tags?.[0];
    if (tag === undefined) {
      root.push(op);
      continue;
    }
    const key = groupKey(tag);
    const list = byKey.get(key) ?? [];
    list.push(op);
    byKey.set(key, list);
  }
  return {
    root,
    groups: [...byKey.entries()].map(([key, ops]) => ({ key, operations: ops })),
  };
}

/** Turns a tag into a camelCase client key (`User Management` → `userManagement`). */
function groupKey(tag: string): string {
  const camel = tag.replace(/[^A-Za-z0-9]+(.)?/g, (_m, c: string | undefined) =>
    c ? c.toUpperCase() : '',
  );
  return camel.charAt(0).toLowerCase() + camel.slice(1);
}

