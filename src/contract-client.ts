// Operation → method signature
// ---------------------------------------------------------------------------

/** The `args` parameter type for an operation, and whether it may be omitted. */
function argsType(op: ContractOperation): { type: string; optional: boolean } {
  const members: string[] = [];
  let required = false;
  if (op.params) {
    members.push(`params: ${tsType(op.params)}`);
    required = true;
  }
  if (op.query) {
    const queryRequired = op.query.kind === 'object' && hasRequiredProp(op.query);
    members.push(`query${queryRequired ? '' : '?'}: ${tsType(op.query)}`);
    required ||= queryRequired;
  }
  if (op.body) {
    members.push(`body: ${tsType(op.body.schema)}`);
    required = true;
  }
  members.push('headers?: Record<string, string>');
  return { type: `{ ${members.join('; ')} }`, optional: !required };
}

/** Whether an object node has at least one required (non-optional) property. */
function hasRequiredProp(node: Extract<SchemaNode, { kind: 'object' }>): boolean {
  return Object.values(node.properties).some((p) => !p.optional);
}

/** The body type of a single response (`Blob` for files, async iterable for SSE). */
function responseBodyType(response: ContractResponse): string {
  if (response.schema) {
    return tsType(response.schema);
  }
  if (response.file) {
    return 'Blob';
  }
  if (response.sse) {
    // SSE: the client yields each parsed event from the stream.
    return `AsyncIterable<${response.sse.schema ? tsType(response.sse.schema) : 'unknown'}>`;
  }
  return 'void';
}

/** Whether an operation has any `text/event-stream` (SSE) response. */
function isStreaming(op: ContractOperation): boolean {
  return Object.values(op.responses).some((r) => r.sse !== undefined);
}

/** Whether an operation has any binary/file response (returned as a `Blob`). */
function isBinary(op: ContractOperation): boolean {
  return Object.values(op.responses).some((r) => r.file !== undefined);
}

/** The success return type: the body of the first declared 2xx response. */
function successType(op: ContractOperation): string {
  const ok = Object.entries(op.responses).find(([status]) => {
    const code = Number(status);
    return code >= 200 && code < 300;
  });
  return ok ? responseBodyType(ok[1]) : 'void';
}

/**
 * The discriminated `{ status, body, headers }` union for `.raw()` — declared
 * statuses only. `headers` is a standard `Headers` object (read with
 * `headers.get('x-total-count')`); response header values aren't individually typed.
 */
function rawType(op: ContractOperation): string {
  const arms = Object.entries(op.responses).map(
    ([status, response]) =>
      `{ status: ${status}; body: ${responseBodyType(response)}; headers: Headers }`,
  );
  return arms.length ? arms.join(' | ') : '{ status: number; body: unknown; headers: Headers }';
}

/** A method type: a call signature returning the success body plus a `.raw` form. */
function methodType(op: ContractOperation): string {
  const { type, optional } = argsType(op);
  const arg = `args${optional ? '?' : ''}: ${type}`;
  return `{ (${arg}): Promise<${successType(op)}>; raw(${arg}): Promise<${rawType(op)}> }`;
}

/** The `Client` interface: root methods plus one nested object per tag group. */
function emitClientInterface(root: ContractOperation[], groups: OperationGroup[]): string {
  const method = (op: ContractOperation, indent: string): string => {
    const doc = jsdoc(indent, {
      summary: op.summary,
      description: op.description,
      deprecated: op.deprecated,
    });
    return `${doc}${indent}${op.operationId}: ${methodType(op)};`;
  };
  const lines = root.map((op) => method(op, '  '));
  for (const group of groups) {
    const inner = group.operations.map((op) => method(op, '    ')).join('\n');
    lines.push(`  ${group.key}: {\n${inner}\n  };`);
  }
  return `export interface Client {\n${lines.join('\n')}\n}`;
}

/** The runtime spec literal for one operation (method/path + multipart/stream flags). */
function specLiteral(op: ContractOperation): string {
  const parts = [`method: ${JSON.stringify(op.method)}`, `path: ${JSON.stringify(op.path)}`];
  if (op.body?.mediaType === 'multipart/form-data') {
    parts.push(`mediaType: 'multipart/form-data'`);
  }
  if (isStreaming(op)) {
    parts.push('stream: true');
  }
  if (isBinary(op)) {
    parts.push('binary: true');
  }
  return `{ ${parts.join(', ')} }`;
}

/** The `createClient` factory wiring each operation to the inlined runtime. */
function emitFactory(root: ContractOperation[], groups: OperationGroup[]): string {
  const rootLines = root.map((op) => `    ${op.operationId}: method(${specLiteral(op)}),`);
  for (const group of groups) {
    const inner = group.operations
      .map((op) => `      ${op.operationId}: method(${specLiteral(op)}),`)
      .join('\n');
    rootLines.push(`    ${group.key}: {\n${inner}\n    },`);
  }
  return `export function createClient(options: ClientOptions): Client {
  const method = (spec: OperationSpec) =>
    Object.assign((args?: RequestArgs) => request(options, spec, args), {
      raw: (args?: RequestArgs) => requestRaw(options, spec, args),
    });
  return {
${rootLines.join('\n')}
  } as unknown as Client;
}`;
}

// ---------------------------------------------------------------------------
// Inlined runtime (emitted verbatim into the standalone client)
// ---------------------------------------------------------------------------

// Authored with String.raw so regex backslashes are literal; intentionally uses
// string concatenation (no template literals / `${}`) so it survives unescaped.
const RUNTIME = String.raw`/** Options for the generated client. */
export interface ClientOptions {
  /** Base URL the operation paths are appended to. */
  baseUrl: string;
  /** Static or lazily-resolved default headers (e.g. auth). */
  headers?: Record<string, string | (() => string | Promise<string>)>;
  /** Custom fetch implementation (defaults to the global fetch). */
  fetch?: typeof fetch;
}

/** Thrown by the default call form on a non-2xx response; carries the parsed body. */
export class ZodecClientError extends Error {
  constructor(
    public readonly status: number,
    public readonly body: unknown,
    public readonly headers: Headers,
  ) {
    super('zodec client: request failed with status ' + status);
    this.name = 'ZodecClientError';
  }
}

interface OperationSpec {
  method: string;
  path: string;
  mediaType?: string;
  stream?: boolean;
  binary?: boolean;
}

interface RequestArgs {
  params?: Record<string, string | number | boolean>;
  query?: Record<string, unknown>;
  body?: unknown;
  headers?: Record<string, string>;
}

interface RawResponse {
  status: number;
  body: unknown;
  headers: Headers;
}

async function resolveHeaders(
  headers: ClientOptions['headers'],
): Promise<Record<string, string>> {
  const out: Record<string, string> = {};
  for (const [key, value] of Object.entries(headers ?? {})) {
    out[key] = typeof value === 'function' ? await value() : value;
  }
  return out;
}

function buildUrl(
  baseUrl: string,
  path: string,
  params?: RequestArgs['params'],
  query?: RequestArgs['query'],
): string {
  const withParams = path.replace(/\{([^}]+)}/g, (_m: string, key: string) =>
    encodeURIComponent(String(params?.[key] ?? '')),
  );
  let url = baseUrl.replace(/\/$/, '') + withParams;
  if (query) {
    const qs = new URLSearchParams();
    for (const [key, value] of Object.entries(query)) {
      if (value === undefined || value === null) continue;
      if (Array.isArray(value)) for (const item of value) qs.append(key, String(item));
      else qs.append(key, String(value));
    }
    const search = qs.toString();
    if (search) url += '?' + search;
  }
  return url;
}

function toFormData(body: unknown): FormData {
  const form = new FormData();
  for (const [key, value] of Object.entries(body as Record<string, unknown>)) {
    if (value === undefined || value === null) continue;
    if (Array.isArray(value)) for (const item of value) form.append(key, item as Blob | string);
    else form.append(key, value as Blob | string);
  }
  return form;
}

async function parseBody(res: Response): Promise<unknown> {
  if (res.status === 204) return undefined;
  const contentType = res.headers.get('content-type') ?? '';
  if (contentType.includes('json')) {
    try {
      return await res.json();
    } catch {
      return undefined;
    }
  }
  const text = await res.text();
  return text === '' ? undefined : text;
}

function isOk(status: number): boolean {
  return status >= 200 && status < 300;
}

async function fetchRaw(
  options: ClientOptions,
  spec: OperationSpec,
  args?: RequestArgs,
): Promise<Response> {
  const fetchFn = options.fetch ?? fetch;
  const url = buildUrl(options.baseUrl, spec.path, args?.params, args?.query);
  const headers = await resolveHeaders(options.headers);
  Object.assign(headers, args?.headers);
  let body: BodyInit | undefined;
  if (args?.body !== undefined) {
    if (spec.mediaType === 'multipart/form-data') {
      body = toFormData(args.body); // the runtime sets the multipart boundary
    } else {
      if (!('content-type' in headers)) headers['content-type'] = 'application/json';
      body = JSON.stringify(args.body);
    }
  }
  if (spec.stream && !('accept' in headers)) headers['accept'] = 'text/event-stream';
  return fetchFn(url, { method: spec.method.toUpperCase(), headers, body });
}

/** Reads a text/event-stream body, yielding each event's parsed data payload. */
async function* sseStream(res: Response): AsyncGenerator<unknown> {
  const stream = res.body;
  if (!stream) return;
  const reader = stream.getReader();
  const decoder = new TextDecoder();
  let buffer = '';
  try {
    for (;;) {
      const { done, value } = await reader.read();
      if (done) break;
      buffer += decoder.decode(value, { stream: true });
      let sep = buffer.indexOf('\n\n');
      while (sep !== -1) {
        const data = parseSseFrame(buffer.slice(0, sep));
        buffer = buffer.slice(sep + 2);
        if (data !== undefined) yield data;
        sep = buffer.indexOf('\n\n');
      }
    }
  } finally {
    await reader.cancel();
  }
}

/** Extracts and parses the data payload from one SSE frame (ignores comments). */
function parseSseFrame(frame: string): unknown {
  const lines: string[] = [];
  for (const line of frame.split('\n')) {
    if (line.startsWith('data:')) lines.push(line.slice(line.startsWith('data: ') ? 6 : 5));
  }
  if (lines.length === 0) return undefined;
  const payload = lines.join('\n');
  try {
    return JSON.parse(payload);
  } catch {
    return payload;
  }
}

async function request(
  options: ClientOptions,
  spec: OperationSpec,
  args?: RequestArgs,
): Promise<unknown> {
  const res = await fetchRaw(options, spec, args);
  if (!isOk(res.status)) {
    throw new ZodecClientError(res.status, await parseBody(res), res.headers);
  }
  if (spec.stream) return sseStream(res);
  if (spec.binary) return res.blob();
  return parseBody(res);
}

async function requestRaw(
  options: ClientOptions,
  spec: OperationSpec,
  args?: RequestArgs,
): Promise<RawResponse> {
  const res = await fetchRaw(options, spec, args);
  let body: unknown;
  if (isOk(res.status) && spec.stream) body = sseStream(res);
  else if (isOk(res.status) && spec.binary) body = await res.blob();
  else body = await parseBody(res);
  return { status: res.status, body, headers: res.headers };
}`;
