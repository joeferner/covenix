/** Renders a schema node as a TypeScript type expression. */
function tsType(node: SchemaNode): string {
  switch (node.kind) {
    case 'ref':
      return node.id;
    case 'string':
      return 'string';
    case 'number':
      return 'number';
    case 'boolean':
      return 'boolean';
    case 'date':
      // A Date travels as an ISO string over JSON, and a types-only client does
      // no revival — so the honest wire type is `string`. Reviving to a real
      // `Date` is tracked for the opt-in runtime-validation feature (the contract
      // still preserves `kind: 'date'`).
      return 'string';
    case 'file':
      return 'File | Blob';
    case 'null':
      return 'null';
    case 'unknown':
    case 'any':
    case 'unsupported':
      return 'unknown';
    case 'literal':
      return node.values.length ? node.values.map(literalType).join(' | ') : 'never';
    case 'enum':
      return node.values.length ? node.values.map((v) => JSON.stringify(v)).join(' | ') : 'never';
    case 'array':
      return `Array<${tsType(node.element)}>`;
    case 'tuple': {
      const items = node.items.map(tsType);
      if (node.rest) {
        items.push(`...Array<${tsType(node.rest)}>`);
      }
      return `[${items.join(', ')}]`;
    }
    case 'union':
    case 'discriminatedUnion':
      return node.variants.length ? `(${node.variants.map(tsType).join(' | ')})` : 'never';
    case 'record':
      return `Record<${tsType(node.key)}, ${tsType(node.value)}>`;
    case 'nullable':
      return `${tsType(node.inner)} | null`;
    case 'optional':
      return `${tsType(node.inner)} | undefined`;
    case 'default':
      return tsType(node.inner);
    case 'object':
      return objectType(node);
  }
}

/** Renders a literal value as a TypeScript literal type. */
function literalType(value: string | number | boolean | null): string {
  return value === null ? 'null' : JSON.stringify(value);
}

/** Renders an object node as a TypeScript object type (`{ a: T; b?: U }`). */
function objectType(node: Extract<SchemaNode, { kind: 'object' }>): string {
  const members = Object.entries(node.properties).map(
    ([name, prop]) => `${propKey(name)}${prop.optional ? '?' : ''}: ${tsType(prop.schema)}`,
  );
  if (node.additionalProperties) {
    members.push(`[key: string]: ${tsType(node.additionalProperties)}`);
  }
  return members.length ? `{ ${members.join('; ')} }` : 'Record<string, never>';
}

/** A property name as a TS key — bare if a valid identifier, else quoted. */
function propKey(name: string): string {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name) ? name : JSON.stringify(name);
}

/**
 * Renders a JSDoc block at the given indent from a description / summary /
 * deprecated flag, or `''` when there's nothing to document.
 */
function jsdoc(
  indent: string,
  parts: {
    summary?: string | undefined;
    description?: string | undefined;
    deprecated?: boolean | undefined;
  },
): string {
  const lines: string[] = [];
  if (parts.summary) {
    lines.push(...parts.summary.split('\n'));
  }
  if (parts.description && parts.description !== parts.summary) {
    if (lines.length > 0) {
      lines.push('');
    }
    lines.push(...parts.description.split('\n'));
  }
  if (parts.deprecated) {
    if (lines.length > 0) {
      lines.push('');
    }
    lines.push('@deprecated');
  }
  if (lines.length === 0) {
    return '';
  }
  if (lines.length === 1) {
    return `${indent}/** ${lines[0]} */\n`;
  }
  const body = lines.map((line) => `${indent} *${line ? ` ${line}` : ''}`).join('\n');
  return `${indent}/**\n${body}\n${indent} */\n`;
}

/** A named object schema as a JSDoc'd `export interface` (one member per line). */
function emitInterface(id: string, node: Extract<SchemaNode, { kind: 'object' }>): string {
  const members = Object.entries(node.properties).map(([name, prop]) => {
    const doc = jsdoc('  ', { summary: prop.description });
    return `${doc}  ${propKey(name)}${prop.optional ? '?' : ''}: ${tsType(prop.schema)};`;
  });
  if (node.additionalProperties) {
    members.push(`  [key: string]: ${tsType(node.additionalProperties)};`);
  }
  return `${jsdoc('', { description: node.description })}export interface ${id} {\n${members.join('\n')}\n}`;
}

/** `export interface`/`export type` declarations for the contract's named schemas. */
function emitSchemas(schemas: Record<string, SchemaNode>): string {
  const decls = Object.entries(schemas).map(([id, node]) =>
    node.kind === 'object' ? emitInterface(id, node) : `export type ${id} = ${tsType(node)};`,
  );
  return decls.join('\n\n');
}

